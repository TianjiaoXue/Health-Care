    public void popluatePie(){    
        DefaultPieDataset dataset = new DefaultPieDataset();
         HashMap<String,Integer> compoundMap=new HashMap<String,Integer>();
         for(Drug drug:disease.getDrugList().getDrugList()){                               
           for(Compound c:drug.getCompoundList().getCompoundList()){     
               String name=c.getCompoundName();
               if(compoundMap.containsKey(name))
                   compoundMap.put(name, compoundMap.get(name)+1);
               else
                    compoundMap.put(name, 1);
           }
         }
           
               Iterator<String> iter=compoundMap.keySet().iterator();
               while(iter.hasNext()){
                   String coName = iter.next();
                   int num=compoundMap.get(coName);
               dataset.setValue(coName, num);
               } 
           
           JFreeChart chart=ChartFactory.createPieChart("Compound", dataset, true, true,true);
           PiePlot p=(PiePlot) chart.getPlot();
           ChartFrame frame=new ChartFrame("Compound",chart);
           DecimalFormat df = new DecimalFormat("0.00%");
           NumberFormat nf = NumberFormat.getNumberInstance();
           StandardPieSectionLabelGenerator sp1 = new StandardPieSectionLabelGenerator("{0}  {2}", nf, df);
           p.setLabelGenerator(sp1);
           frame.setVisible(true);
           frame.setSize(500,500);
    }
    
