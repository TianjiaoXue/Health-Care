    public void popGeneTable(){
        DefaultTableModel dtm = (DefaultTableModel) geneTable.getModel();       
        dtm.setRowCount(0);
        for(Study study:disease.getStudyList().getStudyList()){           
           Object row[]=new Object[4];
           row[0]=disease.getDiseaseName();
           row[1]=study.getStudyGene();
           row[2]=study.getStudyName();
           row[3]=study.getResearcher();
           dtm.addRow(row);
        
    }
    }
    
    public void populateCompoundTable(){
        DefaultTableModel dtm = (DefaultTableModel) compoundTable.getModel();       
        dtm.setRowCount(0);
        for(Drug drug:disease.getDrugList().getDrugList()){                               
           for(Compound c:drug.getCompoundList().getCompoundList()){                         
           Object row[]=new Object[5];
           row[0]=disease.getDiseaseName();
           row[1]=drug.getDrugName();
           row[2]=drug.getManufacture();
           row[3]=c.getCompoundName();
           row[4]=c.getSupplier();
           dtm.addRow(row);                            
            }
        }    
    }
    
    
